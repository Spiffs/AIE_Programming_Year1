void Application::Unload()
{
	delete[] chicken1;
	delete[] chicken2;
	delete[] chicken3;
	delete[] chicken4;
}

void Application::Run()
{
	InitWindow(m_windowWidth, m_windowHeight, "Chichen!");

	Load();

	while (!WindowShouldClose())
	{
		float dt = GetFrameTime();
		Update(dt);
		Draw();
	}

	Unload();

	CloseWindow();
}

void Application::Update(float dt)
{
	if (IsKeyPressed(KEY_TAB))
	{
		if (debug)
			debug = false;
		else
			debug = true;
	}

	chicken1->Update(dt);
	chicken2->Update(dt);
	chicken3->Update(dt);
	chicken4->Update(dt);
	m_graphEditor->Update(dt);
}

void Application::Draw()
{
	BeginDrawing();
	ClearBackground(RAYWHITE);

	DrawTextureEx(m_background, { 0, 0 }, 0.0f, 1.0f, WHITE);

	if (debug)
		m_graphEditor->Draw();

	chicken1->Draw();
	chicken2->Draw();
	chicken3->Draw();
	chicken4->Draw();

	EndDrawing();
}



